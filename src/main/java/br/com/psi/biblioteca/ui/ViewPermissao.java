    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (txtNome.getText().toString().equals(null) && txtDescricao.getText().toString().equals(null) && txtId.getText().toString().equals(null)
                || txtNome.getText().toString().equals("") && txtDescricao.getText().toString().equals("") && txtId.getText().toString().equals("")) {
            JOptionPane.showMessageDialog(null, "Selecione quem você deseja Editar!");

        } else {
            Permissao p = SimpleFactoryUtil.criarPermissao();
            try {
                p = facade.buscarPermissao(Long.parseLong(txtId.getText().toString()));
                p.setNome(txtNome.getText().toString());
                p.setDescricao(txtDescricao.getText().toString());
                facade.editarPermissao(p);
                JOptionPane.showMessageDialog(null, "Permissão editada com sucesso!");
            } catch (DaoException ex) {
                Logger.getLogger(ViewPermissao.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Não foi possível editar essa permissão!");
            } finally {
                btnAdicionar.setEnabled(true);
                limparCampos();
                povoarTabelaPermissoes();
            }

        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (txtNome.getText().toString().equals(null) && txtDescricao.getText().toString().equals(null) && txtId.getText().toString().equals(null)
                || txtNome.getText().toString().equals("") && txtDescricao.getText().toString().equals("") && txtId.getText().toString().equals("")) {
            JOptionPane.showMessageDialog(null, "Selecione quem você deseja Excluir!");

        } else {
            Permissao p = SimpleFactoryUtil.criarPermissao();
            try {
                p = facade.buscarPermissao(Long.parseLong(txtId.getText().toString()));

                facade.removerPermissao(p, p.getId());
                JOptionPane.showMessageDialog(null, "Permissão Removida com sucesso!");
                povoarTabelaPermissoes();

            } catch (DaoException ex) {
                Logger.getLogger(ViewPermissao.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Não foi possível Excluir essa permissão!");
            } finally {
                btnAdicionar.setEnabled(true);
                limparCampos();
                povoarTabelaPermissoes();
            }

        }
    }//GEN-LAST:event_btnExcluirActionPerformed

