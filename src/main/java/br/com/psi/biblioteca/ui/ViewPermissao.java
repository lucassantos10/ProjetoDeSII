    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if (txtNome.getText().toString().equals(null) && txtDescricao.getText().toString().equals(null) && txtId.getText().toString().equals(null)
                || txtNome.getText().toString().equals("") && txtDescricao.getText().toString().equals("") && txtId.getText().toString().equals("")) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar permissões, Dados inválidos!");
        } else {
            Permissao p = SimpleFactoryUtil.criarPermissao();
            p.setNome(txtNome.getText().toString());
            p.setDescricao(txtDescricao.getText().toString());

            try {
                facade.salvarPermissao(p);
                JOptionPane.showMessageDialog(null, "Permissão Cadastrada com Sucesso!");

            } catch (Exception e) {
                Logger.getLogger(ViewPermissao.class.getName()).log(Level.SEVERE, null, e);
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar Permissão!");
            } finally {
                limparCampos();
                povoarTabelaPermissoes();
            }

        }

    }//GEN-LAST:event_btnAdicionarActionPerformed

