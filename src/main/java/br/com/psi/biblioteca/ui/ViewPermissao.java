    private void povoarTabelaPermissoes() {
        DefaultTableModel table = (DefaultTableModel) tblPermissao.getModel();

        Facade facade = new Facade();
        List<Permissao> lista;
        try {
            lista = facade.listarPermissoes();
            while (table.getRowCount() > 0) {
                table.removeRow(0);
            }

            for (Permissao p : lista) {
                table.addRow(new Object[]{
                    p.getId(),
                    p.getNome(),
                    p.getDescricao()
                });
            }
        } catch (DaoException ex) {
            Logger.getLogger(ViewPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void setarCampos() {
        int setar = tblPermissao.getSelectedRow();//
        txtNome.setText(tblPermissao.getModel().getValueAt(setar, 1).toString());
        txtDescricao.setText(tblPermissao.getModel().getValueAt(setar, 2).toString());
        txtId.setText(tblPermissao.getModel().getValueAt(setar, 0).toString());
        btnAdicionar.setEnabled(false);

    }

    private void limparCampos() {
        txtNome.setText("");
        txtDescricao.setText("");
        txtId.setText("");
    }
    
    


